    private void utsTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_utsTextFieldKeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if(!(Character.isDigit(c)
                ||(c==KeyEvent.VK_BACK_SPACE)
                ||(c==KeyEvent.VK_DELETE))){
            getToolkit().beep();
            JOptionPane.showMessageDialog(null, "Nilai Bukan Numerik");
            evt.consume();
            
        }
    }//GEN-LAST:event_utsTextFieldKeyTyped

