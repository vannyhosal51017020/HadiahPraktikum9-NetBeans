    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int uts = Integer.parseInt(utsTextField.getText());
        int tm  = Integer.parseInt(tmTextField.getText());
        int uas = Integer.parseInt(uasTextField.getText());
        
        String nama = jTextField1.getText().toUpperCase();
        String grade = null;
        String hasil = null;
        
        if(uts>=0&&uts<=100&&tm>=0&&tm<=100&&uas>=0&&uas<=100){
            int rata =(uts+tm+uas)/3;
            
            if (rata>=90){
                grade = "A";
                hasil = "Lulus";
            }
            if (rata>=80&&rata<=89){
                grade = "B";
                hasil = "Lulus";
            }
            if (rata>=70&&rata<=79){
                grade = "C";
                hasil = "Lulus";
            }
            else if(rata<=70) {
                grade = "D";
                hasil = "Tidak Lulus";
            }
            NamaLabel.setText("Nama : "+nama );
            nilaiLabel.setText("Nilai Rata-Rata : "+String.valueOf(rata) );
            gradeLabel.setText("Grade : "+grade );
            hasilLabel.setText("Hasil : "+hasil );
        }   
        else{ 
            JOptionPane.showMessageDialog(null, "Nilai bukan numerik","nilai tidak antara 0-100", JOptionPane.ERROR_MESSAGE);
            jTextField1.setText("");
            utsTextField.setText("");
            tmTextField.setText("");
            uasTextField.setText("");
        
            }
    
    }//GEN-LAST:event_jButton1ActionPerformed

